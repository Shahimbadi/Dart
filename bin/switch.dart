void main() {

  String day="tue";
  switch(day){
    case "mon":
      print("Monday");
      break;
    case "tue":
      print("Tuesday");
      break;
    case "wed":
      print("Wednesday");
      break;
    case "thu":
      print("Thursday");
      break;
    case "fri":
      print("Friday");
      break;
    case "sat":
      print("Saturday");
      break;
    default:
      print("Invalid");
  }

}